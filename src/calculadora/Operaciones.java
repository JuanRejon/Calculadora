public class Operaciones { //clase padre llamada Operaciones Basicas
    
    //interface OperacionesAvanzadas 
    //clase hija Calculadora
    
    public double suma(double a, double b){
        return a + b;
    }
    
    public double resta(double a, double b){
        return a - b;
    }
    
    public double multi(double a, double b){
        return a * b;
    }
    
    public double divis(double a, double b){
        return a / b;
    }
}
